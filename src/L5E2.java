import java.util.Scanner;
public class L5E2 {
    public static void main(String[] args) {
        //create a program to count down from 10.
        for (int count = 10; count > 0; count--) {
            //setting up fixed loop
            //count set to 10 and will start at 10.
            //termination when reaching 0
            //count-- means counting down from initial number individually until 0.
            System.out.println(count);
        }
        System.out.println("Blastoff!");
    }
}
