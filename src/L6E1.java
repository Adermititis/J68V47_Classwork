import java.util.Scanner;
public class L6E1 {
    public static void main(String[] args) {
        //write a loop to print all even numbers
        //between 10 and 24.
        int count = 10;
        //starting at 10.
        while (count <= 24){
            //while loop checks the count is less than 24.
            System.out.println(count);
            count+=2;
            //asks to add 2 to current count.
        }
    }
}
